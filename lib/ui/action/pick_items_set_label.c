void pick_items_set_title(void)
{
    // Your code here
    // set text title
    switch (device_data_get_item_selected())
    {
    case E_ITEM_NONE:
        LOG_DEBUG("E_ITEM_NONE");
        break;
    case E_ITEM_BUTLER_DELIVERY:
        lv_label_set_text(ui_Label19, "Butler Delivery");
        break;
    case E_ITEM_CANNON:
        lv_label_set_text(ui_Label19, "Cannon");
        break;
    case E_ITEM_SHORT_PASS:
        lv_label_set_text(ui_Label19, "Short Pass");
        break;
    default:
        lv_label_set_text(ui_Label19, "Cannon");
        break;
    }
}

